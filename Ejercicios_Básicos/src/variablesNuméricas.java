public class variablesNuméricas {
	private int n;
	private int m;
	public variablesNuméricas () {
		n = 3;
		m = 7;
	}
	public variablesNuméricas (int n, int m) {
		this.n = n;
		this.m = m;
	}
	public int getN() {
		return n;
	}
	public void setN(int n) {
		this.n = n;
	}
	public int getM() {
		return m;
	}
	public void setM(int m) {
		this.m = m;
	}
	
}
